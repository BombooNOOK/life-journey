/**
 * personalDayLines.ts
 *
 * 目的：
 * - パーソナルデイの一言メッセージを、月の流れも含めて自然に変化させる
 * - 日記の日付横に添えられる、短くてやわらかい一行を返す
 * - 同じ日なら同じ文、年や人が変わると違う文になりやすいようにする
 *
 * 設計：
 * - monthNumber: 1..9
 * - dayNumber: 1..9
 * - 81組み合わせ
 * - 各組み合わせごとに4候補
 * - 固定抽選（seedで選ぶ）
 */

export type DayNumber = 1 | 2 | 3 | 4 | 5 | 6 | 7 | 8 | 9;
export type MonthNumber = 1 | 2 | 3 | 4 | 5 | 6 | 7 | 8 | 9;

export type PersonalDayLineContext = {
  year: number;
  month: number; // calendar month 1..12
  day: number; // day of month 1..31
  personalMonthNumber: MonthNumber;
  personalDayNumber: DayNumber;
  userId?: string;
};

export type LineSet = string[];

export type PersonalDayMonthLineMap = Record<MonthNumber, Record<DayNumber, LineSet>>;

/**
 * 軽いハッシュ
 * - 同じ userId なら同じ値
 * - userId がない場合は 0
 */
export function hashString(input?: string): number {
  if (!input) return 0;
  let h = 0;
  for (let i = 0; i < input.length; i++) {
    h = (h * 31 + input.charCodeAt(i)) >>> 0;
  }
  return h;
}

/**
 * seed を作る
 * - 年 / カレンダー月 / 日 / personalMonth / personalDay / userId を混ぜる
 * - 同じ日なら同じ文
 * - 年・人が変わると変わりやすい
 */
export function buildPersonalDayLineSeed(ctx: PersonalDayLineContext): number {
  const userHash = hashString(ctx.userId);
  return (
    ctx.year * 100000 +
    ctx.month * 1000 +
    ctx.day * 10 +
    ctx.personalMonthNumber +
    ctx.personalDayNumber +
    userHash
  );
}

/**
 * 固定抽選で1件選ぶ
 */
export function pickLineBySeed(lines: string[], seed: number): string {
  if (!lines.length) return "";
  const index = Math.abs(seed) % lines.length;
  return lines[index] ?? lines[0] ?? "";
}

/**
 * 実際に日付横へ表示する一行を返す
 */
export function getPersonalDayLine(ctx: PersonalDayLineContext): string {
  const lines =
    personalDayMonthLines[ctx.personalMonthNumber]?.[ctx.personalDayNumber] ?? [];
  const seed = buildPersonalDayLineSeed(ctx);
  return pickLineBySeed(lines, seed);
}

