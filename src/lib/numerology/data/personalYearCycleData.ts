/** `personalYearData` と同一参照（既存コードの import 名用）。 */
export const PERSONAL_YEAR_CYCLE_DATA = personalYearData;

export function personalYearCycleEntry(cycleNumber: number): PersonalYearCycleEntry {
  const n = Math.floor(Math.abs(cycleNumber));
  if (n >= 1 && n <= 9) {
    return personalYearData[n];
  }
  return {
    number: n,
    theme: `（周期 ${n}）`,
    subtitle: "",
    article: "",
  };
}
