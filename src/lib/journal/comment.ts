function hashSeed(parts: Array<number | string>): number {
  const raw = parts.join("|");
  let hash = 0;
  for (let i = 0; i < raw.length; i++) {
    hash = (hash * 31 + raw.charCodeAt(i)) >>> 0;
  }
  return hash;
}

function pickFrom(lines: string[], seed: number): string {
  if (lines.length === 0) return "";
  return lines[seed % lines.length];
}

function buildCycleNumbers(params: {
  birthMonth: number | null;
  birthDay: number | null;
  now: Date;
}): { py: number | null; pm: number | null; pd: number | null } {
  const { birthMonth, birthDay, now } = params;
  if (!birthMonth || !birthDay) {
    return { py: null, pm: null, pd: null };
  }
  const year = now.getFullYear();
  const month = now.getMonth() + 1;
  const day = now.getDate();
  const py = personalYearNumber(birthMonth, birthDay, year);
  const pm = personalMonthNumber(py, month);
  const pd = personalDayNumber(pm, day);
  return { py, pm, pd };
}

export function normalizeJournalCommentText(text: string): string {
  const normalized = text
    // 不自然になりやすい先頭句を除去
    .replace(/^今日はどちらかというと、\s*/g, "")
    .replace(/^今日はどちらかというと\s*/g, "")
    .replace(/^今日の記録を読むと、\s*/g, "")
    .replace(/^今日の記録を読むと\s*/g, "")
    .replace(/^記録を読むと、\s*/g, "")
    .replace(/^記録を読むと\s*/g, "")
    .replace(/^今日は、\s*/g, "")
    .replace(/^今日は\s*/g, "")
    // 句読点の重複を軽く補正
    .replace(/、、+/g, "、")
    .replace(/。。+/g, "。")
    .replace(/、。/g, "。")
    .replace(/\s{2,}/g, " ")
    .trim();

  return normalized;
}

